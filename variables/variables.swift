
import UIKit


// in swift we can declare variables using var and let keywords:
// var - variable

var name = "John"
var age = 30

// let - constant
let city = "London"
let country = "UK"

// we can also declare variables using type inference:
// var - variable
var name_two:String = "John"
// let - constant
let city:String = "London"
let country:String = "UK"

