import UIKit

// Switch Statement

// Syntax

/*
 switch some value to consider {
 case value 1:
     respond to value 1
 case value 2,
      value 3:
     respond to value 2 or 3
 default:
     otherwise, do something else
 }
 */


let char = "a";

switch char {
case "a":
    print("The character is a");
case "b":
    print("The character is b");
default:
    print("The character is other than a and b")
}

